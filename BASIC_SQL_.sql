CREATE DATABASE STUDENTRECORD;
USE STUDENTRECORD;

-- CREATE TABLE
CREATE TABLE STUDENTS ( ID INT AUTO_INCREMENT PRIMARY KEY,
                                                    NAME VARCHAR (20),
                                                    AGE INT NOT NULL,
                                                    COURSE VARCHAR (10),
                                                    MARKS INT );
                                                    
